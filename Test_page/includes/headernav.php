    <!---- Script for night/darkmode WITH cache ---->
  <script>
  const checkbox = document.querySelector('.nightmode_button input[type="checkbox"]');
  const body = document.querySelector('body');

  // Function to set the dark mode class and cache the preference
  function setDarkMode(enabled) {
    if (enabled) {
      body.classList.add('dark');
      body.classList.remove('light');
    } else {
      body.classList.add('light');
      body.classList.remove('dark');
    }
    localStorage.setItem('nightModeEnabled', enabled);
  }

  // Check if there's a cached preference and apply it on page load
  document.addEventListener('DOMContentLoaded', () => {
    const cachedPreference = localStorage.getItem('nightModeEnabled');

    // If there's a cached preference, apply it
    if (cachedPreference !== null) {
      setDarkMode(cachedPreference === 'true');
      checkbox.checked = cachedPreference === 'true';
    }
  });

  // Listen for changes on the toggle button and update the dark mode accordingly
  checkbox.addEventListener('change', function () {
    setDarkMode(this.checked);
  });
</script>
    
    
